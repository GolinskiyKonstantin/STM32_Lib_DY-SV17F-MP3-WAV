//---------------------------------------------------------------------------------

// установить режим повтора от 0 до 7
// 0x00 - воспр. все песни покругу
// 0x01 - воспр. текущую песню по кругу
// 0x02 - воспр. текущую песню 1 раз и останавливаеться
// 0x03 - воспр. рандомную песню
// 0x04 - воспр. попорядку в текущей папке
// 0x05 - воспр. рандомно в текущей папке
// 0x06 - воспр. все песни по порядку в текущей папке и останавливаеться
// 0x07 - воспр. все песни и останавливаеться
void SV17F_Set_Replay_mode( UART_HandleTypeDef *UART, uint8_t mode ){
	
	uint8_t data[5] = { 0xAA, 0x18, 0x01, mode };
	data[4] = data[0] + data[1] + data[2] + data[3];
	HAL_UART_Transmit( UART, data, sizeof( data ), 1000 );
}
//-------------------------------------------------------------------------------------------------------------------------

