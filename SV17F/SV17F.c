// установка громкости от 0 до 30 по умолчанию стоит 20
void SV17F_Set_Volume( UART_HandleTypeDef *UART, uint8_t volume ){
	
	uint8_t data[5] = { 0xAA, 0x13, 0x01, volume };
	data[4] = data[0] + data[1] + data[2] + data[3];
	HAL_UART_Transmit( UART, data, sizeof( data ), 1000 );
}
