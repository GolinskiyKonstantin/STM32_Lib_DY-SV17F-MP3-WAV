//-----------------------------------------------------------------------------------------------

void SV17F_Volume_down( UART_HandleTypeDef *UART );
//-----------------------------------------------------------------------------------------------

void SV17F_Previous_file( UART_HandleTypeDef *UART );
//-----------------------------------------------------------------------------------------------

void SV17F_Next_file( UART_HandleTypeDef *UART );
