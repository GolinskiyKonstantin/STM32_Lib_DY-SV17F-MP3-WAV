// установить диск откуда воспроизводить будем
#define NO_DEVICE		0xFF
#define USB_DEVICE		0x00
#define SD_DEVICE		0x01
#define FLASH_DEVICE	0x02

// установить эквалайзер
#define OFF				0x00
#define POP				0x01
#define ROCK			0x02
#define JAZZ			0x03
#define CLASSIC			0x04




/* Functions prototypes ----------------------------------------------*/

