//-------------------------------------------------------------------------------------------------------------------------

// установить режим повтора от 0 до 7
// 0x00 - воспр. все песни покругу
// 0x01 - воспр. текущую песню по кругу
// 0x02 - воспр. текущую песню 1 раз и останавливаеться
// 0x03 - воспр. рандомную песню
// 0x04 - воспр. попорядку в текущей папке
// 0x05 - воспр. рандомно в текущей папке
// 0x06 - воспр. все песни по порядку в текущей папке и останавливаеться
// 0x07 - воспр. все песни и останавливаеться
void SV17F_Set_Replay_mode( UART_HandleTypeDef *UART, uint8_t mode );
//-------------------------------------------------------------------------------------------------------------------------

void SV17F_Set_Replay_time( UART_HandleTypeDef *UART, uint16_t time );
//-------------------------------------------------------------------------------------------------------------------------

// указываем номер песни и она сама воспроизводится
void SV17F_Set_song( UART_HandleTypeDef *UART, uint16_t song );
//-------------------------------------------------------------------------------------------------------------------------

// установить эквалайзер
void SV17F_Set_EQ( UART_HandleTypeDef *UART, uint8_t EQ );
//-------------------------------------------------------------------------------------------------------------------------

// установить диск откуда воспроизводить будем
void SV17F_Set_Disk( UART_HandleTypeDef *UART, uint8_t disk );
//-------------------------------------------------------------------------------------------------------------------------

// указываем путь
void SV17F_Set_Path( UART_HandleTypeDef *UART, uint8_t disk, uint8_t path );
//-------------------------------------------------------------------------------------------------------------------------


// указываем номер песни без воспроизведения
void SV17F_Set_song_noPlay( UART_HandleTypeDef *UART, uint16_t song );
